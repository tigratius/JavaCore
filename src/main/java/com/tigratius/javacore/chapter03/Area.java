package main.java.com.alex.javacore.chapter03;

//Вычисляем площадь круга
public class Area {
    public static void main(String[] args) {

        double pi, r, area;

        r = 10.8;
        pi = 3.1416;
        area = pi * r * r;

        System.out.println("Площадь круга  равна: " + area);
    }
}
